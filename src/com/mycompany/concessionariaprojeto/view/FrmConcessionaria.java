    private void jButtonCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCadastrarMouseClicked
        String marca = jTextFieldMarca.getText();
        String modelo = jTextFieldModelo.getText();
        BigDecimal preco = new BigDecimal(jTextFieldPreco.getText());

        jTextFieldMarca.setText("");
        jTextFieldModelo.setText("");
        jTextFieldPreco.setText("");
        concessionariaController.cadastrar(marca, modelo, preco);
        buscarPreencherTabela();
    }//GEN-LAST:event_jButtonCadastrarMouseClicked

    private void jButtonExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonExcluirMouseClicked
        int linhaSelecionada = jTable1.getSelectedRow();
        String id = jTable1.getValueAt(linhaSelecionada, 0).toString();
        concessionariaController.excluir(Integer.valueOf(id));
        buscarPreencherTabela();
    }//GEN-LAST:event_jButtonExcluirMouseClicked

    private void buscarPreencherTabela() {
        dados = concessionariaController.buscar();
        jTable1.setModel(dados);
    }

    private void jButtonListarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonListarMouseClicked
        buscarPreencherTabela();
    }//GEN-LAST:event_jButtonListarMouseClicked

